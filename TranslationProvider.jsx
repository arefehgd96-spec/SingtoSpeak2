export function TranslationProvider({ children }) {
  const [locale, setLocale] = useState('fa');
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const loadUserLocale = async () => {
      try {
        const user = await base44.auth.me();
        if (user?.locale) {
          setLocale(user.locale);
          // Set document direction and language
          document.documentElement.dir = (user.locale === 'ar' || user.locale === 'fa') ? 'rtl' : 'ltr';
          document.documentElement.lang = user.locale;
        } else if (user?.app_language) {
          const localeMap = {
            'english': 'en',
            'german': 'de',
            'persian': 'fa',
            'arabic': 'ar'
          };
          const newLocale = localeMap[user.app_language] || 'fa';
          setLocale(newLocale);
          document.documentElement.dir = (newLocale === 'ar' || newLocale === 'fa') ? 'rtl' : 'ltr';
          document.documentElement.lang = newLocale;
          await base44.auth.updateMe({ locale: newLocale });
        }
      } catch (e) {
        console.error('Failed to load user locale:', e);
      } finally {
        setLoading(false);
      }
    };
    loadUserLocale();
  }, []);

  const t = (key) => {
    if (loading) return key;
    return translations[locale]?.[key] || translations['en']?.[key] || key;
  };

  const changeLanguage = async (newLocale) => {
    setLocale(newLocale);
    try {
      await base44.auth.updateMe({ locale: newLocale });
      // Update document direction immediately
      document.documentElement.dir = (newLocale === 'ar' || newLocale === 'fa') ? 'rtl' : 'ltr';
      document.documentElement.lang = newLocale;
    } catch (e) {
      console.error('Failed to update locale:', e);
    }
  };

  return (
    <TranslationContext.Provider value={{ t, locale, changeLanguage, loading }}>
      {children}
    </TranslationContext.Provider>
  );
}

export function useTranslation() {
  const context = useContext(TranslationContext);
  if (!context) {
    throw new Error('useTranslation must be used within TranslationProvider');
  }
  return context;
}